---@param config ZecolorConfig?
Zecolor.setup = function(config)
    Zecolor.config = vim.tbl_deep_extend("force", Zecolor.config, config or {})
end

--- main load function
Zecolor.load = function()
    if vim.version().minor < 8 then
        vim.notify_once("Zecolor.nvim: you must use neovim 0.8 or higher")
        return
    end

    -- reset colors
    if vim.g.colors_name then
        vim.cmd.hi("clear")
    end
    vim.g.colors_name = "zecolor"
    vim.o.termguicolors = true

    local groups = get_groups()

    -- add highlights
    for group, settings in pairs(groups) do
        vim.api.nvim_set_hl(0, group, settings)
    end
end

return Zecolor
